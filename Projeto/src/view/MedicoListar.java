    private void btMedicoListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMedicoListarActionPerformed
        PacienteHasMedico paciente;
        
        String crm = textListarPeloCRM.getText();
        PacienteHasMedicoDAO pacienteDao = new PacienteHasMedicoDAO();
        
        if (crm.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Digite um crm.",
                    "Busca", JOptionPane.INFORMATION_MESSAGE);
        } else {
            
            listaPacientesH = pacienteDao.buscarPacienteHasMedico();

            if (listaPacientesH!= null ) {
                mostraDados(Integer.parseInt(crm));          
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não encontrado!",
                        "Busca", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btMedicoListarActionPerformed

