    private void mostraDados(int crm) {
        DefaultListModel DLM = new DefaultListModel<>();
        Paciente paciente;
        ManipuladorPacientes manipPacientes = new ManipuladorPacientes();

        for(PacienteHasMedico pacienteNaLista : listaPacientesH){
            if(pacienteNaLista.getCrm()== crm){
                paciente = manipPacientes.buscaPorCpf(pacienteNaLista.getCpf());
                DLM.addElement("Paciente: " + paciente.getNome());
                
            }
                
        }
       
        listLista.setModel(DLM);
    }
