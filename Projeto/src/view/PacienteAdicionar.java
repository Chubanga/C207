    private void btTelaPacienteAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTelaPacienteAdicionarActionPerformed
        
        if (passouNaPreValidacaoPaciente()) {
            try {
                cadastraPaciente();
                verificaCadastrarNovamente();    
                    } catch (ValorInvalidoException | ErroCadastroException ex) {
                        JOptionPane.showMessageDialog(rootPane, ex.getMessage(),
                                "Erro", JOptionPane.ERROR_MESSAGE);
                        limpaCampos();
                    }
        }
        else{
            verificaCadastrarNovamente();
        }
       
        
    }//GEN-LAST:event_btTelaPacienteAdicionarActionPerformed

